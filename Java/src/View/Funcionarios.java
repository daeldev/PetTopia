    private void listarFuncionarios() {
        // Instância a classe de conexão
        HttpConnection httpConnection = new HttpConnection();

        // Chama o método para listar os funcionários cadastrados
        List<FuncionarioModel> funcionarios = httpConnection.listarFuncionarios();

        // Obtem o modelo da tabela
        DefaultTableModel Tabela = (DefaultTableModel) jTFuncionarios.getModel();

        // Limpa a tabela antes de adicionar os novos dados
        Tabela.setRowCount(0);
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        
        // Itera sobre a lista de funcionários e adiciona os dados na tabela
        for (FuncionarioModel funcionario : funcionarios) {
            // Verifica se o tipo de funcionário não é ADMINISTRADOR
                Object[] dados = {
                    funcionario.getNome_funcionario(), 
                    funcionario.getCpf_funcionario().replaceAll("(\\d{3})(\\.\\d{3}\\.\\d{3}-)(\\d{2})", "***$2**"),
                    funcionario.getEmail(),
                    funcionario.getTipoFuncionario().substring(0, 1).toUpperCase() + funcionario.getTipoFuncionario().substring(1).toLowerCase(),
                    funcionario.getData_emissao().format(formatter),
                    funcionario.getId_funcionario()
                };
                Tabela.addRow(dados);
        }
    }
    
