    private void salvarRelatorio(byte[] relatorio, String nomePadraoArquivo) {
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Salvar Relatório");
        fileChooser.setSelectedFile(new java.io.File(nomePadraoArquivo)); // Define o nome padrão do arquivo

        // Mostra o diálogo de salvar
        int userSelection = fileChooser.showSaveDialog(null);

        if (userSelection == JFileChooser.APPROVE_OPTION) {
            java.io.File arquivoSelecionado = fileChooser.getSelectedFile();
            String caminhoArquivo = arquivoSelecionado.getAbsolutePath();

            // Garante que o arquivo tenha a extensão .pdf
            if (!caminhoArquivo.toLowerCase().endsWith(".pdf")) {
                caminhoArquivo += ".pdf";
            }

            try (FileOutputStream fos = new FileOutputStream(caminhoArquivo)) {
                // Escreve o conteúdo do byte[] no arquivo
                fos.write(relatorio);
                fos.flush();
                JOptionPane.showMessageDialog(null, "Relatório salvo com sucesso em: " + caminhoArquivo);
            } catch (IOException e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(null, "Erro ao salvar o relatório: " + e.getMessage());
            }
        }
    }
    
    private void jBGerarRelatorioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBGerarRelatorioActionPerformed
        // Cria o DTO para o relatório de funcionários
        FuncionarioRelatorioDTO relatorioDTO = new FuncionarioRelatorioDTO();

        // Lista para armazenar os funcionários
        List<FuncionarioModel> funcionarios = new ArrayList<>();

        // Obtém o modelo da tabela
        DefaultTableModel tableModel = (DefaultTableModel) jTFuncionarios.getModel();  // Assumindo que a tabela de funcionários é jTFuncionarios

        // Itera sobre as linhas da tabela para criar os objetos FuncionarioModel
        for (int i = 0; i < tableModel.getRowCount(); i++) {
            FuncionarioModel funcionario = new FuncionarioModel();

            // Configura as propriedades do funcionário
            funcionario.setNome_funcionario((String) tableModel.getValueAt(i, 0)); // Nome
            funcionario.setCpf_funcionario((String) tableModel.getValueAt(i, 1)); // CPF
            funcionario.setEmail((String) tableModel.getValueAt(i, 2)); // E-mail
            funcionario.setTipoFuncionario((String) tableModel.getValueAt(i, 3).toString().toUpperCase()); // Tipo de Funcionário
            
            String dataEmissaoString = (String) tableModel.getValueAt(i, 4); // Data do pedido como String
            LocalDate dataEmissao = LocalDate.parse(dataEmissaoString, DateTimeFormatter.ofPattern("dd/MM/yyyy"));
            funcionario.setData_emissao(dataEmissao); // Nascimento
            
            funcionario.setId_funcionario((int) tableModel.getValueAt(i, 5)); // ID

            // Adiciona o funcionário à lista
            funcionarios.add(funcionario);
        }

        // Adiciona os funcionários à lista do relatório
        relatorioDTO.setFuncionarios(funcionarios);

        // Configura o nome do administrador no FuncionarioModel
        FuncionarioModel administrador = new FuncionarioModel();
        administrador.setNome_funcionario(this.nomeFuncionario); // Substitua pelo nome real do administrador
        relatorioDTO.setAdministrador(administrador);

        // Chama o método para gerar o relatório
        HttpConnection httpConnection = new HttpConnection();

        // Aqui você vai enviar o JSON dinâmico
        FuncionarioRelatorioDTO response = httpConnection.gerarRelatorioFuncionarios(relatorioDTO);

        if (response.getRelatorio() != null) {
            // Define o nome padrão do arquivo
            String nomePadraoArquivo = "RelatorioFuncionarios.pdf";

            // Salva o relatório no sistema
            salvarRelatorio(response.getRelatorio(), nomePadraoArquivo);
        } else {
            JOptionPane.showMessageDialog(null, "ERRO: falha ao tentar gerar o relatório de funcionários.");
        }
    }//GEN-LAST:event_jBGerarRelatorioActionPerformed

