    private void jBEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBEditarActionPerformed
        // TODO add your handling code here:
        int[] selectedRows = jTFuncionarios.getSelectedRows();

        if(selectedRows.length == 0){
            JOptionPane.showMessageDialog(null, "ERRO: selecione um funcionário.");
        }else if(selectedRows.length > 1){
            JOptionPane.showMessageDialog(null, "ERRO: selecione somente um funcionário.");
        }else{
            FuncionarioModel funcionarioSelecionado = new FuncionarioModel();
            funcionarioSelecionado.setTipoFuncionario(String.valueOf(jTFuncionarios.getValueAt(jTFuncionarios.getSelectedRow(), 3)));
            funcionarioSelecionado.setEmail(String.valueOf(jTFuncionarios.getValueAt(jTFuncionarios.getSelectedRow(), 2)));
            
            if (!funcionarioSelecionado.getTipoFuncionario().equals("Administrador") || funcionarioSelecionado.getEmail().equals(this.emailFuncionario)){
                funcionarioSelecionado.setId_funcionario(Integer.parseInt(String.valueOf(jTFuncionarios.getValueAt(jTFuncionarios.getSelectedRow(), 5))));

                VisualizarFuncionario visualizarFuncionario = new VisualizarFuncionario(funcionarioSelecionado.getId_funcionario(), this.emailFuncionario);
                DashboardAdministrador.add(visualizarFuncionario);
                visualizarFuncionario.setVisible(true);
                visualizarFuncionario.setBounds(400, 0, 410, 744);
            }else{
                JOptionPane.showMessageDialog(null, "ERRO: ação não permitida.");
            }
        }
    }//GEN-LAST:event_jBEditarActionPerformed

