    private void listarComprovantesRenda() {
        // Instância a classe de conexão
        HttpConnection httpConnection = new HttpConnection();

        // Chama o método para listar os funcionários cadastrados
        List<ComprovanteRendaModel> comprovantes = httpConnection.listarComprovantes();

        // Obtem o modelo da tabela
        DefaultTableModel Tabela = (DefaultTableModel) jTComprovantes.getModel();

        // Limpa a tabela antes de adicionar os novos dados
        Tabela.setRowCount(0);
        
        // Itera sobre a lista de funcionários e adiciona os dados na tabela
        for (ComprovanteRendaModel comprovante : comprovantes) {
            // Exibi somente os pedidos pendentes ou cancelados
            if (!comprovante.getStatus().equals("CONCLUIDO")){
                Object[] dados = {
                    comprovante.getCliente().getNome(),
                    comprovante.getAnimal().getNome(),   
                    comprovante.getStatus().substring(0, 1).toUpperCase() + comprovante.getStatus().substring(1).toLowerCase(),
                    comprovante.getIdDocumento()
                };
                Tabela.addRow(dados);
            }
        }
    }
    
