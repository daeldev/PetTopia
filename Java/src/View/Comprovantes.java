    private void jBVisualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBVisualizarActionPerformed
        // TODO add your handling code here:
        int[] selectedRows = jTComprovantes.getSelectedRows();

        if(selectedRows.length == 0){
            JOptionPane.showMessageDialog(null, "ERRO: selecione um comprovante.");
        }else if(selectedRows.length > 1){
            JOptionPane.showMessageDialog(null, "ERRO: selecione somente um comprovante.");
        }else{
            // Seta os dados fornecidos para o DenunciaModel
            ComprovanteRendaModel comprovanteRendaModel = new ComprovanteRendaModel();
            comprovanteRendaModel.setIdDocumento(Integer.parseInt(jTComprovantes.getValueAt(jTComprovantes.getSelectedRow(), 3).toString()));
            
            AnalisarPedidoAdocao visualizarComprovante = new AnalisarPedidoAdocao(dashboard, comprovanteRendaModel.getIdDocumento(), this.emailFuncionario, this.funcao);
            dashboard.add(visualizarComprovante);
            visualizarComprovante.setVisible(true);
            visualizarComprovante.setBounds(400, 0, 701, 455);
        }
    }//GEN-LAST:event_jBVisualizarActionPerformed

